//Variable with Inferred Type
void main() {
  var name = 'Afzal'; // Dart knows it's a String
  var age = 25;       // Dart knows it's an int
  var price = 99.99;  // Dart infers double

  print('Name: $name');
  print('Age: $age');
  print('Price: $price');
}

